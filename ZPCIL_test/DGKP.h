#include <iostream>
using namespace std;

extern float Z1, Z2;                        // ВХЯКЮ ГСАЭЕБ ЬЕЯРЕПМХ Х ЙНКЕЯЮ
extern float MN, BE, X1, X2, X[2];          // ЛНДСКЭ, СЦНК МЮЙКНМЮ, ЙНЩТТХЖХЕМРШ ЯЛЕЫЕМХЪ

extern fstream f_1;    // ТЮИК ДКЪ ПЕГСКЭРЮРЮ    //***

void DGKP(int I12, float DB[2], float BETB, float DP[2], float ROP[2], float DL[2], float ROL[2], float DELZV, float DG[2], float ROG[2], float DU[2], float DV[2],
          float RLM, float RLMIN, float RKEPS, float PALF, float PX, float SNA[2], float TETP[2], float TETV[2], float DZTP[2], float DZTV[2]) 
{
    f_1 << "\n\n          днонкмхрекэмше ценлерпхвеяйхе х " << endl;
    f_1 << "          йхмелюрхвеяйхе оюпюлерпш ";

    if (I12 == 0)     f_1 << "\n                                           ьеярепмъ  йнкеян ";   // ПЕДСЙРНП
             else f_1 << "\n                                              йнкеян  ьеярепмъ ";   // ЛСКЭРХОКХЙЮРНП

    f_1 << "\nнямнбмше дхюлерпш                    DB " << setw(10) << DB[0] << setw(10) << DB[1];
    f_1 << "\nдхюлерпш нйпсфмняреи мхфмху";
    f_1 << "\nюйрхбмшу рнвей опнтхкъ               DP " << setw(10) << DP[0] << setw(10) << DP[1];
    f_1 << "\nпюдхсяш йпхбхгмш юйрхбмнцн";
    f_1 << "\nопнтхкъ гсаю б мхфмеи рнвйе         ROP " << setw(10) << ROP[0] << setw(10) << ROP[1];

    f_1 << "\nдхюлерпш нйпсфмняреи цпюмхвмшу";
    f_1 << "\nрнвей опнтхкъ                        DL " << setw(10) << DL[0] << setw(10) << DL[1];
    f_1 << "\nпюдхсяш йпхбхгмш опнтхкъ гсаю";
    f_1 << "\nцпюмхвмни рнвйе                     ROL " << setw(10) << ROL[0] << setw(10) << ROL[1];

    if (DELZV != 0.) {
        f_1 << "\nдхюлерпш нйпсфмняреи лндхтхйюжхх";
        f_1 << "\nцнкнбнй гсаэеб                        DG " << setw(10) << DG[0] << setw(10) << DG[1];
        f_1 << "\nпюдхсяш йпхбхгмш опнтхкъ гсаю";
        f_1 << "\nб мювюкэмни рнвйе лндхтхйюжхх         ROG " << setw(10) << ROG[0] << setw(10) << ROG[1];

    }
    if (BE == 0.) {
        f_1 << "\nдхюлерпш нйпсфмняреи бепумху цпюмхв-";
        f_1 << "\nмшу рнвей ндмноюпмнцн гюжеокемхъ      DU " << setw(10) << DU[0] << setw(10) << DU[1];
        f_1 << "\nдхюлерпш нйпсфмняреи мхфмху цпюмхв-";
        f_1 << "\nмшу рнвей ндмноюпмнцн гюжеокемхъ      DV " << setw(10) << DV[0] << setw(10) << DV[1];
    }

    f_1 << "\n\nьюц гюжеокемхъ                     PALF " << setw(14) << PALF;
            
    ZPRGMS(BETB, GRM, SEC);

    if (BE != 0.) {
        f_1 << "\n\nняебни ьюц                           PX " << setw(14) << PX;
        f_1 << "\n\nнямнбмни сцнк мюйкнмю кхмхх гсаю BETB  " << setprecision(2) << fixed << setw(13) << GRM << "." << setw(2) << SEC << endl;
    }
    f_1 << "\nйнщттхжхемрш мнплюкэмни рнкыхмш" << endl;
    f_1 << "гсаю мю онбепумнярх бепьхм          SNA " << setprecision(3) << fixed << setw(10) << SNA[0] << setw(10) << SNA[1] << endl;
    f_1 << "сдекэмне яйнкэфемхе б мхфмеи" << endl;
    f_1 << "рнвйе юйрхбмнцн опнтхкъ            TETP " << setw(10) << TETP[0] << setw(10) << TETP[1] << endl;

    if (BE == 0.) {
        f_1 << "\nсдекэмне яйнкэфемхе б мхфмеи цпюмхв-" << endl;
        f_1 << "мни рнвйе ндмноюпмнцн гюжеокемхъ    TETV " << setw(10) << TETV[0] << setw(10) << TETV[1] << endl;
    }
    if (BE != 0.) {
        f_1 << "\nяпедмъъ ясллюпмюъ дкхмю" << endl;
        f_1 << "йнмрюйрмшу кхмхи                     LM " << setw(14) << RLM << endl;
        f_1 << "мюхлемэьюъ ясллюпмюъ дкхмю" << endl;
        f_1 << "йнмрюйрмшу кхмхи                   LMIN " << setw(14) << RLMIN << endl;
        f_1 << "йнщттхжхемр япедмецн хглемемхъ" << endl;
        f_1 << "ясллюпмни дкхмш йнмрюйрмшу кхмхи   KEPS " << setw(14) << RKEPS << endl;
    }

}

